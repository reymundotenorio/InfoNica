                <h4 id="llegar">Cómo llegar</h4>
            </div>

<p>Si sale desde Managua o Granada, el trayecto se hace en avión, carro o barco hasta San Carlos, y desde San Carlos se toma una de las lanchas que 
diariamente llegan hasta El Castillo.</p>

<p><b>Managua/Granada a San Carlos en carro:</b></p>
<p>Los cerca de 300 Kms de viaje se completan en unas 4 a 5 horas. Las recientes mejoras de asfaltado lo hacen cómodo y una oportunidad para conocer 
los bellos paisajes de la región de Chontales y Río San Juan de Nicaragua. Existe servicio de parqueo en el muelle de San Carlos.</p>

<p><b>Desde Managua</b> siga la Panamericana en dirección norte hasta el empalme de San Benito, donde tomará la carretera con dirección a Juigalpa, Chontales. 
Desviarse hacia Acoyapa y proseguir hacia San Carlos.</p>

<p><b>Desde Granada</b> viaje en dirección a Masaya y allí tome el desvío hacia Tipitapa para dar con la Panamericana. A la altura de San Benito tome 
la carretera a Juigalpa, Chontales. Tome por último el desvío hacia Acoyapa y prosiga en dirección sur hasta San Carlos.</p>

<p><b>Managua/Granada a San Carlos en avión:</b></p>

<p>La Costeña tiene un viaje diario a San Carlos desde el Aeropuerto Internacional de Managua con salida a la 1:00 p.m. y 
con una duración aproximada de 45 minutos. Si está en Granada, pregunte  a su hotel por un servicio de transporte al aeropuerto.
 
<p><b>Managua/Granada a San Carlos en barco:</b></p>
<p>La Compañía Portuaria Nacional ofrece servicios desde Granada hacia San Carlos los días Lunes y Jueves a las 2:00 p.m., haciendo parada en la Isla de Ometepe,
 Morrito y San Miguelito, llegando a destino a las 5:00 a.m. del siguiente día. Es un trayecto de 15 a 16 horas de duración aproximadamente. </p>
 
<p><b>Desde San Carlos a El Castillo</b></p>
<p>Una vez en San Carlos, la manera más sencilla de llegar hasta el Castillo es tomar una de las lanchas rápidas. Su duración de 1 hora y media.</p>

<p><b>Horarios de salida San Carlos – El Castillo lancha rápida:</b> 10:25am, 04:30pm. Otra alternativa es tomar una lancha lenta, con una duración de 
trayecto de 3 horas. Aunque es un viaje más largo, muchos disfrutarán de la experiencia cultural y paisajística al ver a los pobladores del Río entrar y 
salir de la lancha mientras la presencia de la naturaleza se engrandece a medida que nos acercamos a El Castillo.</p>

<p><b>Los horarios de salida San Carlos – El Castillo lancha lenta:</b> 5:20 a.m., 10:30 a.m., 12:30 p.m., 3:30 p.m.</p>
 

