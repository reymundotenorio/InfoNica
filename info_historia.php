<p>El Castillo de la Inmaculada Concepción es una fortaleza de la época colonial, ubicada en el departamento de Río San Juan, Nicaragua.
Fue construida a finales del siglo XVII sobre las ruinas de una antigua fortaleza de la época del rey Felipe II de España. La elección del
lugar se debe a la intervención del ingeniero militar Martín de Andújar Cantos quién había llegado a la zona junto a Fernando Francisco de 
Escobedo con el fin de realizar una inspección. El motivo de la construcción de la nueva fortaleza era detener las invasiones de los piratas
que incursionaban por ese río para saquear la ciudad de Granada. Entre el 29 de julio y el 3 de agosto de1762 fue sitiado por los ingleses
en la batalla del Río San Juan de Nicaragua y se salvó la fortaleza por la valentía de Rafaela Herrera, hija de don Pedro Herrera comandante
del fuerte, quien acababa de morir cuando venían los piratas.</p>

<p>Según los relatos, en la tarde del 2 de agosto (quinto día del sitio), en plena batalla, un gorrión entró a la capilla del castillo y cantó
delante de la imagen de la Virgen María, en su advocación de la Inmaculada Concepción, salió y volvió a entrar allí. Al día siguiente los sitiadores
se retiraron y los sitiados lo interpretaron como un milagro por el suceso del gorrión en la capilla.</p>

<p>Años más tarde en 1780, siendo comandante del castillo don Juan de Ayssa, volvió a ser atacado por los ingleses, esta vez al mando de un joven 
capitán de la Real Marina Británica, llamado Horacio Nelson (quien moriría con el grado de almirante el 21 de octubre de 1805 al final de la Batalla 
de Trafalgar), el cual tomó el castillo después de varios días de sitio; pero una posterior epidemia de cólera mató a más ingleses que el asedio a la 
fortaleza, por lo que el futuro héroe de Trafalgar fue llevado a Jamaica casi sin vida.</p>

<p>El Castillo de la Inmaculada Concepción, construida por los españoles entre 1673 y 1675 para impedir las incursiones de los 
piratas que subían por el río San Juan para acceder luego al lago Cocibolca y atacar desde éste la ciudad de Granada.A lo largo del siglo XVIII la 
fortaleza sufrió varias veces el asedio de las tropas británicas. En 1780 un ataque dirigido por Lord George Germain con la intención de tomar la 
plaza y asegurar el control británico del lago Nicaragua contó entre sus filas con un joven capitán de nombre Horacio Nelson que se encargó de dirigir 
parte de las tropas a través de la selva para atacar la fortaleza desde una loma en la retaguardia que hoy en día sigue recibiendo el nombre de Lomas de Nelson.
Hoy en día la fortaleza alberga un museo y una biblioteca y se encuentra en la lista de lugares que en breve serán nominados por la Unesco como patrimonio 
de la humanidad.
